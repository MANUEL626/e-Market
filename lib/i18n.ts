const dictionaries: Record<AppLocale, Dictionary> = { fr, en, de, zh };

export function normalizeLocale(locale: string | null | undefined): AppLocale {
  return locale === "en" || locale === "de" || locale === "zh" ? locale : DEFAULT_LOCALE;
}

export function getProfileLocale(profile: MemberMeResponse | null | undefined): AppLocale {
  return normalizeLocale(profile?.params?.locale);
}

export function translate(locale: string | null | undefined, key: string): string {
  const normalized = normalizeLocale(locale);
  return dictionaries[normalized][key] ?? dictionaries[DEFAULT_LOCALE][key] ?? key;
}

export function languageLabel(locale: string | null | undefined): string {
  const normalized = normalizeLocale(locale);
  return LOCALE_OPTIONS.find((item) => item.value === normalized)?.label ?? "Français";
}
